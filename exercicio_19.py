#19) Ler dois valores (considere que não serão lidos valores iguais) e escrever o maior deles. 
print( "Apresentando valores ! ")
valor1 = int(input( "Apresentando o primeiro valor: "))
valor2 = int(input( "Apresentando o segundo valor: "))

if valor1 > valor2:
    print ( "O maior valor é: ", valor1)
else:
    print ( "O maior valor é: ", valor2)
